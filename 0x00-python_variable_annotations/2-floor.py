#!/usr/bin/env python3
"""
Takes a float n as argument and returns the floor of the float.
"""
import math


def floor(n: float) -> int:
    """
    Takes a float n as argument and returns the floor of the float.

    Args:
        n (float): The float argument.

    Returns:
        int: The floor of n as an integer.
    """
    return math.floor(n)
