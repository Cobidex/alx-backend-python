#!/usr/bin/env python3
'''
Add two float arguments and return their sum as a float.
'''

def add(a: float, b: float) -> float:
    '''
    Add two float arguments and return their sum as a float.

    Args:
        a (float): The first float argument.
        b (float): The second float argument.

    Returns:
        float: The sum of a and b.
    '''
    return a + b
